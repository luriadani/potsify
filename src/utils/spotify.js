import { getValidToken } from './auth'

const BASE = 'https://api.spotify.com/v1'

async function apiFetch(path, options = {}) {
  const token = await getValidToken()
  if (!token) throw new Error('Not authenticated')

  const res = await fetch(`${BASE}${path}`, {
    ...options,
    headers: {
      Authorization: `Bearer ${token}`,
      'Content-Type': 'application/json',
      ...(options.headers || {}),
    },
  })

  if (!res.ok) {
    const err = await res.json().catch(() => ({}))
    throw new Error(err?.error?.message || `API error ${res.status}`)
  }

  return res.json()
}

// Broader genre list for more results
const OBSCURE_GENRES = [
  'ambient', 'acoustic', 'folk', 'blues', 'classical',
  'jazz', 'indie', 'country', 'emo', 'post-rock',
  'shoegaze', 'bossanova', 'new-age', 'sleep', 'study',
  'soul', 'r-n-b', 'gospel', 'reggae', 'ska',
  'punk', 'metal', 'grunge', 'pop', 'dance',
]

// Raised default threshold — more tracks will qualify
export const MAX_POPULARITY = 40

/**
 * Fetch low-popularity tracks for a given genre.
 */
export async function fetchLowPopularityTracks(genre, maxPop = MAX_POPULARITY, count = 20) {
  const offset = Math.floor(Math.random() * 200)

  const params = new URLSearchParams({
    q: `genre:${genre}`,
    type: 'track',
    limit: 50,
    offset,
    market: 'US',
  })

  const data = await apiFetch(`/search?${params}`)
  const tracks = data.tracks?.items || []

  return tracks
    .filter(
      (t) =>
        t &&
        t.popularity <= maxPop &&
        t.popularity >= 0  // include tracks with 0 popularity too
    )
    .sort((a, b) => a.popularity - b.popularity)
    .slice(0, count)
    .map(normalizeTrack)
}

/**
 * Fetch a mix of low-popularity tracks across multiple genres.
 */
export async function fetchDiscoveryFeed(maxPop = MAX_POPULARITY, perGenre = 10) {
  const selectedGenres = shuffleArray(OBSCURE_GENRES).slice(0, 6)

  const results = await Promise.allSettled(
    selectedGenres.map((g) => fetchLowPopularityTracks(g, maxPop, perGenre))
  )

  const tracks = results
    .filter((r) => r.status === 'fulfilled')
    .flatMap((r) => r.value)

  // Dedupe by track id
  const seen = new Set()
  return tracks.filter((t) => {
    if (seen.has(t.id)) return false
    seen.add(t.id)
    return true
  })
}

/**
 * Search tracks by name, filtered to low popularity.
 * Uses a higher threshold for search so users actually get results.
 */
export async function searchLowPopularityTracks(query, maxPop = MAX_POPULARITY) {
  const params = new URLSearchParams({
    q: query,
    type: 'track',
    limit: 50,
    market: 'US',
  })

  const data = await apiFetch(`/search?${params}`)
  const tracks = data.tracks?.items || []

  return tracks
    .filter((t) => t && t.popularity <= maxPop && t.popularity >= 0)
    .sort((a, b) => a.popularity - b.popularity)
    .slice(0, 40)
    .map(normalizeTrack)
}

/**
 * Get the current user's Spotify profile.
 */
export async function getCurrentUser() {
  return apiFetch('/me')
}

/**
 * Create a new playlist and add tracks to it.
 */
export async function createPlaylist(userId, tracks) {
  const playlist = await apiFetch(`/users/${userId}/playlists`, {
    method: 'POST',
    body: JSON.stringify({
      name: '🟢 Potsify — Undiscovered Tracks',
      description:
        'Songs with popularity score below 40 — the forgotten side of Spotify. Generated by Potsify.',
      public: true,
    }),
  })

  const uris = tracks.map((t) => `spotify:track:${t.id}`)
  for (let i = 0; i < uris.length; i += 100) {
    await apiFetch(`/playlists/${playlist.id}/tracks`, {
      method: 'POST',
      body: JSON.stringify({ uris: uris.slice(i, i + 100) }),
    })
  }

  return playlist
}

// ─── Helpers ────────────────────────────────────────────────────────────────

function normalizeTrack(t) {
  return {
    id: t.id,
    name: t.name,
    artists: t.artists.map((a) => a.name),
    album: t.album.name,
    albumArt: t.album.images?.[1]?.url || t.album.images?.[0]?.url || null,
    popularity: t.popularity,
    previewUrl: t.preview_url,
    spotifyUrl: t.external_urls?.spotify,
    uri: t.uri,
    durationMs: t.duration_ms,
  }
}

function shuffleArray(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

